# essa função controla o fluxo principal do sistema
def executar_sistema():
    conn = conectar_banco()

    if conn is None:
        return

    criar_tabela_se_necessario(conn)
    atualizar_arquivos(conn)

    while True:
        limpar_tela()
        exibir_menu()

        while True:
            escolha = ler_inteiro_positivo("Escolha uma opção: ")
            if 1 <= escolha <= 6:
                break
            else:
                print("Escolha apenas opções de 1 a 6.")

        match escolha:
            case 1:
                cadastrar_producao(conn)

            case 2:
                listar_producoes(conn)

            case 3:
                excluir_producao(conn)

            case 4:
                estimar_producao()

            case 5:
                consultar_estimativas()

            case 6:
                print("\nEncerrando o sistema...")
                conn.close()
                break

            case _:
                print("\nOpção inválida.")
                pausar()


# esse trecho inicia o sistema
if __name__ == "__main__":
    executar_sistema()

