# essa função transforma os dados do banco em lista de dicionários para o JSON
def converter_para_dicionarios(dados):
    lista_registros = []

    for linha in dados:
        registro = {
            "id": linha[0],
            "tipo_animal": linha[1],
            "categoria": linha[2],
            "quantidade": linha[3],
            "rendimento_total": float(linha[4]),
            "unidade": linha[5]
        }
        lista_registros.append(registro)

    return lista_registros


# essa função atualiza automaticamente os arquivos TXT, JSON e CSV
def atualizar_arquivos(conn):
    try:
        dados = buscar_todos_os_registros(conn)

        # gera o TXT
        with open(ARQUIVO_TXT, "w", encoding="utf-8") as arquivo:
            arquivo.write("RELATÓRIO DE PRODUÇÃO ANIMAL\n")
            arquivo.write("=" * 50 + "\n\n")

            if len(dados) == 0:
                arquivo.write("Não há registros cadastrados.\n")
            else:
                for linha in dados:
                    arquivo.write(f"ID: {linha[0]}\n")
                    arquivo.write(f"Tipo Animal: {linha[1]}\n")
                    arquivo.write(f"Categoria: {linha[2]}\n")
                    arquivo.write(f"Quantidade: {linha[3]}\n")
                    arquivo.write(f"Rendimento Total: {linha[4]}\n")
                    arquivo.write(f"Unidade: {linha[5]}\n")
                    arquivo.write("-" * 50 + "\n")

        # gera o JSON
        with open(ARQUIVO_JSON, "w", encoding="utf-8") as arquivo:
            json.dump(converter_para_dicionarios(dados), arquivo, ensure_ascii=False, indent=4)

        # gera o CSV
        df = pd.DataFrame(
            dados,
            columns=[
                "id",
                "tipo_animal",
                "categoria",
                "quantidade",
                "rendimento_total",
                "unidade"
            ]
        )
        df.to_csv(ARQUIVO_CSV, index=False, encoding="utf-8-sig")

    except Exception as erro:
        print("\nErro ao atualizar arquivos automáticos:")
        print(erro)


