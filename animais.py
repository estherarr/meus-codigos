# essa função exclui uma produção do banco
def excluir_producao(conn):
    limpar_tela()
    exibir_titulo()
    print("EXCLUSÃO DE PRODUÇÃO\n")

    try:
        registro_id = ler_inteiro_positivo("Digite o ID da produção que deseja excluir: ")
        registro = consultar_registro_por_id(conn, registro_id)

        if registro is None:
            print("\nProdução não encontrada.")
            pausar()
            return

        print("\nProdução encontrada:")
        exibir_registro(registro)

        if confirmar("\nDeseja realmente excluir esta produção? (s/n): "):
            cursor = conn.cursor()
            cursor.execute("DELETE FROM producao_animal WHERE id = :1", [registro_id])
            conn.commit()
            cursor.close()

            atualizar_arquivos(conn)

            print("\nProdução excluída com sucesso!")
        else:
            print("\nExclusão cancelada.")

    except Exception as erro:
        print("\nErro ao excluir produção:")
        print(erro)

    pausar()

