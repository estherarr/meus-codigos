    HISTORICO_ESTIMATIVAS.append(estimativa)

    print("\nESTIMATIVA GERADA COM SUCESSO!")
    print(f"Animal: {tipo_animal}")
    print(f"Quantidade planejada: {quantidade}")
    print(f"Média por animal: {media} {unidade}")
    print(f"Produção estimada: {producao_estimada} {unidade}")

