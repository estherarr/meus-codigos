# essa função lê um número inteiro positivo
# isso só vai aparecer quando o usuario digitar um número com vírgula ou uma letra, por exemplo.
# É só uma validação simples
def ler_inteiro_positivo(mensagem):
    while True:
        try:
            numero = int(input(mensagem).strip())
            if numero < 0:
                print("Digite um número inteiro positivo.")
            else:
                return numero
        except ValueError:
            print("Digite apenas números inteiros.")


'''essa função lê um número decimal positivo, no caso é utilizado variáveis que
podem ser "valores quebrados", como o rendimento'''
def ler_float_positivo(mensagem):
    while True:
        try:
            numero = float(input(mensagem).strip().replace(",", "."))
            if numero < 0:
                print("Digite um número positivo.")
            else:
                return numero
        except ValueError:
            print("Digite apenas números válidos.")


# essa função lê um texto e não deixa o campo ficar vazio
def ler_texto_nao_vazio(mensagem):
    while True:
        texto = input(mensagem).strip()
        if texto == "":
            print("Este campo não pode ficar vazio.")
        else:
            return texto


# essa função pede uma confirmação simples ao usuário
def confirmar(mensagem):
    resposta = input(mensagem).strip().lower()
    return resposta in ("s", "sim")


