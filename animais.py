# essa função busca todos os registros da tabela
def buscar_todos_os_registros(conn):
    cursor = conn.cursor()
    cursor.execute("""
        SELECT id, tipo_animal, categoria, quantidade, rendimento_total, unidade
        FROM producao_animal
        ORDER BY id
    """)
    dados = cursor.fetchall()
    cursor.close()
    return dados


# essa função busca apenas um registro pelo ID
def consultar_registro_por_id(conn, registro_id):
    cursor = conn.cursor()
    cursor.execute("""
        SELECT id, tipo_animal, categoria, quantidade, rendimento_total, unidade
        FROM producao_animal
        WHERE id = :1
    """, [registro_id])
    registro = cursor.fetchone()
    cursor.close()
    return registro


